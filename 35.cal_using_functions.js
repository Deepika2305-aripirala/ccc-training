function add(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function mul(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}

let a = 20, b = 5;

console.log("Addition:", add(a, b));
console.log("Subtraction:", sub(a, b));
console.log("Multiplication:", mul(a, b));
console.log("Division:", div(a, b));